namespace RingCentral
{
    public class CallQueueInfo
    {
        /// <summary>
        /// Link to a call queue
        /// </summary>
        public string uri;

        /// <summary>
        /// Internal identifier of a call queue
        /// </summary>
        public string id;

        /// <summary>
        /// Extension number of a call queue
        /// </summary>
        public string extensionNumber;

        /// <summary>
        /// Name of a call queue
        /// </summary>
        public string name;
    }
}