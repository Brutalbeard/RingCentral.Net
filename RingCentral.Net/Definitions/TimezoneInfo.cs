namespace RingCentral
{
    public class TimezoneInfo
    {
        /// <summary>
        /// Internal identifier of a timezone
        /// </summary>
        public string id;

        /// <summary>
        /// Canonical URI of a timezone
        /// </summary>
        public string uri;

        /// <summary>
        /// Short name of a timezone
        /// </summary>
        public string name;

        /// <summary>
        /// Meaningful description of the timezone
        /// </summary>
        public string description;
    }
}