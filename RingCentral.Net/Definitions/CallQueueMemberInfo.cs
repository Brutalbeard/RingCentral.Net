namespace RingCentral
{
    public class CallQueueMemberInfo
    {
        /// <summary>
        /// Link to a call queue member
        /// </summary>
        public string uri;

        /// <summary>
        /// Internal identifier of a call queue member
        /// </summary>
        public long? id;

        /// <summary>
        /// Extension number of a call queue member
        /// </summary>
        public string extensionNumber;
    }
}