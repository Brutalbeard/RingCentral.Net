namespace RingCentral
{
    public class DeviceExtensionInfo
    {
        /// <summary>
        /// Internal identifier of an extension
        /// </summary>
        public long? id;

        /// <summary>
        /// Link to an extension resource
        /// </summary>
        public string uri;

        /// <summary>
        /// Number of extension
        /// </summary>
        public string extensionNumber;
    }
}